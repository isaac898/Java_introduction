package chapter1;

public class CircleProperties {
    public static void main(String[] args) {
        // radius equals 5.5
        //display area and perimeter
        double RADIUS = 5.5 ;
        double PI = 3.14;
        double perimeter = 2 * RADIUS * PI;
        double area = RADIUS * RADIUS * PI;
        System.out.println(perimeter);
        System.out.println(area);

    }
}
